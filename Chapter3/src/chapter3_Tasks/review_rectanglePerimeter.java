package chapter3_Tasks;

public class review_rectanglePerimeter {

	public static void main(String[] args)
	{
		//Classifies x, y and perim as int variables.
		int x, y, perim;
		
		
		x = 13;//Sets length
		y = 4;//Sets width
		perim = (2*x)+(2*y); //Calculates total perimeter of the rectangle
		System.out.println("The perimiter of a rectangle with the width of 4 and the length of 13 is " + perim);

	}

}

